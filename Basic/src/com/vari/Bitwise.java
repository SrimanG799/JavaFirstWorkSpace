package com.vari;

public class Bitwise {
	public static void main(String[] args)
	{
	int a=10;
	int b=15;
	char c='a';
	char d='b';
	System.out.println(a&b);
	System.out.println(a|b);
	System.out.println(a^b);
	System.out.println(c&d);
	System.out.println(c|d);
	System.out.println(c^d);
	System.out.println(12&13);
	System.out.println(11|13);
	System.out.println(14^16);
	}
}
