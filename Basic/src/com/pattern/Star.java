package com.pattern;

public class Star {
	public static void main(String[] args)
	{
		for(int i=0;i<6;i++)
		{
			for(int j=0;j<=i;j++)
			{
				System.out.println('*');
			}
			
			System.out.println(" ");
			
		}
		for(int k=0;k<=10;k++)
		{
			for(int l=k;l<=10;l++)
			{
			System.out.println('*');
		}
			System.out.print(" ");
	}
		for(int i=0;i<=15;i++)
		{
			System.out.print('*');
		}

}
}
