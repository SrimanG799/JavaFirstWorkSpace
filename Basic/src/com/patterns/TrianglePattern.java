package com.patterns;

public class TrianglePattern {
	public static void main(String[] args)
	{
		int r=5;
		int k=1;
	
		for(int i=0;i<=r;i++)
			
		{
			
			
			for(int j=0;j<=i;j++)
			{
				System.out.print(k+" ");
				
				
				
			}
			
			System.out.println();
			k++;
			
		
		}
	}

}
