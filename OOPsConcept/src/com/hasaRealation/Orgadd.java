package com.hasaRealation;

public class Orgadd {
	String state;
	String dist;
	String location;
	public Orgadd(String state, String dist, String location) {
		super();
		this.state = state;
		this.dist = dist;
		this.location = location;
	}
	@Override
	public String toString() {
		return "Orgadd [state=" + state + ", dist=" + dist + ", location=" + location + "]";
	}
	

}
