package com.hasaRealation;

public class Adresss {
	String state;
	String dist;
	public Adresss(String state, String dist) {
		super();
		this.state = state;
		this.dist = dist;
	}
	@Override
	public String toString() {
		return "Adresss [state=" + state + ", dist=" + dist + "]";
	}
	

}
